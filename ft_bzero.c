#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			x;
	unsigned char	*z;

	z = (unsigned char *)s;
	x = 0;
	while (x < n)
	{
		*z = 0;
		x++;
		z++;
	}
}
